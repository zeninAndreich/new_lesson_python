"""
Задание №5

Создать список на 10 элементов, заполненный целыми случайными числами (диапазон на выбор, но конец диапазона больше 5).
Удалить все элементы, которые меньше 5 и найти среднее арифметическое.
Среднее арифметическое значение = сумма всех элементов, разделенная на количество элементов.
"""
import random

spisok = []
h = int(input('Введите конец диапазона(он должен быть больше 5): '))
for i in range(10):
    e = random.randint(0,h)
    spisok.append(e)
print(spisok)
# выбрасываем из списка элементы,которые меньше 5
spisok = [i for i in spisok if i > 5]
print(spisok)
#выведем среднеарифметическое значение.
import statistics
print(statistics.mean(spisok))

"""
Задание №6

Создать список на 20 элементов, заполненный целыми случайными числами от 0 до 20.
Необходимо найти произведение элементов кратных 3, имеющих четный индекс.
"""

import random
spisok = []
j = 1

for i in range(20):
    spisok.append(random.randint(0,20))

print(spisok)

for l in range(len(spisok)):
    if l % 2 == 0:
        if l % 3 == 0:
            j *= spisok[l]
print(spisok[l], ' ', l)
print(j)

