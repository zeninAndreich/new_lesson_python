"""

Задание №1

Из введенной пользователем строки создать список. Удалить из этого списка все буквы ‘a’, ‘e’, ‘u’.
Строка вводится на английском и символы для удаления так же на английском.
"""

st = list(input('Введите строку на английском языке: '))
for i in range(st.count('a')):
    st.remove('a')
for i in range(st.count('e')):
    st.remove('e')
for i in range(st.count('u')):
    st.remove('u')

print(''.join(st))

